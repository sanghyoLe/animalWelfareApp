package com.example.animal_project.MultipleAnswerResult;

public class WaterTimeData {
    private String ratio;
    private String penLocation;
    private String dongTotalCowSize;
    private String answerCowSize;
    private String answer;
    private String score;


    public String getPenLocation() {
        return penLocation;
    }

    public void setPenLocation(String penLocation) {
        this.penLocation = penLocation;
    }



    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public String getDongTotalCowSize() {
        return dongTotalCowSize;
    }

    public void setDongTotalCowSize(String dongTotalCowSize) {
        this.dongTotalCowSize = dongTotalCowSize;
    }



    public String getAnswerCowSize() {

        return answerCowSize;
    }

    public void setAnswerCowSize(String answerCowSize) {
        this.answerCowSize = answerCowSize;
    }


    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getRatio() {
        return ratio;
    }

    public void setRatio(String ratio) {
        this.ratio = ratio;
    }





}
